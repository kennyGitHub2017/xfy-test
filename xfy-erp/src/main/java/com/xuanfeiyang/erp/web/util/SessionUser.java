package com.xuanfeiyang.erp.web.util;

/**
 * 描述一个session 用户数据
 * @author Adam
 *
 */
public class SessionUser {
	
	/*
	 * 用户Id
	 */
	private Integer _userId;
	
	/**
	 * 姓名
	 */
	private String _name;
	
	/**
	 * 登录名
	 */
	private String _username;
	
	/**
	 * 是否管理员
	 */
	private boolean _admin;
	
	/**
	 * 卖家ID
	 */
	private int _sellerId;
	
	/**
	 * 卖家状态
	 */
	private int _sellerStatus;
	
	private int _type;

	public int getType() {
		return _type;
	}

	public void _type(int _type) {
		this._type = _type;
	}

	public int getSellerStatus() {
		return _sellerStatus;
	}

	public void _sellerStatus(int sellerStatus) {
		this._sellerStatus = sellerStatus;
	}

	public int getSellerId() {
		return _sellerId;
	}

	public void _sellerId(int sellerId) {
		this._sellerId = sellerId;
	}

	public Integer getUserId() {
		return _userId;
	}

	public void _userId(Integer userId) {
		this._userId = userId;
	}

	public String getName() {
		return _name;
	}

	public void _name(String name) {
		this._name = name;
	}

	public String getUsername() {
		return _username;
	}

	public void _username(String username) {
		this._username = username;
	}

	public boolean isAdmin() {
		return _admin;
	}

	public void _admin(boolean admin) {
		this._admin = admin;
	}
	
}
