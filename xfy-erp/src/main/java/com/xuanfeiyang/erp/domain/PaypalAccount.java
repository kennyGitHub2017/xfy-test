package com.xuanfeiyang.erp.domain;

import java.math.BigDecimal;
import java.util.Date;

/**
 * Paypal账号
 * @author kenny
 *
 */
public class PaypalAccount {

	private Long id;
	private String account; //paypal账号
	private String apiUserName;//api用户名
	private String apiPassword;//api密码
	private String apiSignature;//api签名
	private String ebayAccount;//所属ebay账号
	private BigDecimal fees;//费率
	private Date createdTime;//记录添加时间
	private Date lastUpdatedTime;//记录最后更新时间
	
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getAccount() {
		return account;
	}
	public void setAccount(String account) {
		this.account = account;
	}
	public String getApiUserName() {
		return apiUserName;
	}
	public void setApiUserName(String apiUserName) {
		this.apiUserName = apiUserName;
	}
	public String getApiPassword() {
		return apiPassword;
	}
	public void setApiPassword(String apiPassword) {
		this.apiPassword = apiPassword;
	}
	public String getApiSignature() {
		return apiSignature;
	}
	public void setApiSignature(String apiSignature) {
		this.apiSignature = apiSignature;
	}
	public String getEbayAccount() {
		return ebayAccount;
	}
	public void setEbayAccount(String ebayAccount) {
		this.ebayAccount = ebayAccount;
	}
	public BigDecimal getFees() {
		return fees;
	}
	public void setFees(BigDecimal fees) {
		this.fees = fees;
	}
	public Date getCreatedTime() {
		return createdTime;
	}
	public void setCreatedTime(Date createdTime) {
		this.createdTime = createdTime;
	}
	public Date getLastUpdatedTime() {
		return lastUpdatedTime;
	}
	public void setLastUpdatedTime(Date lastUpdatedTime) {
		this.lastUpdatedTime = lastUpdatedTime;
	}
	
	
	
}
