package com.xuanfeiyang.erp.domain;

/**
 * 一个简单的带有跟踪号和账户信息的订单对象
 * 
 * @author Adam
 *
 */
public class SimpleOrderWithAccount {

	private Integer orderId;

	private String trackNumber;

	private String accountName;

	private String ebayToken;

	private String ebayDevid;

	private String ebayAppid;

	private String ebayCertid;

	private String ebayRuName;

	private String ebayApiServerUrl;

	public Integer getOrderId() {
		return orderId;
	}

	public void setOrderId(Integer orderId) {
		this.orderId = orderId;
	}

	public String getTrackNumber() {
		return trackNumber;
	}

	public void setTrackNumber(String trackNumber) {
		this.trackNumber = trackNumber;
	}

	public String getAccountName() {
		return accountName;
	}

	public void setAccountName(String accountName) {
		this.accountName = accountName;
	}

	public String getEbayToken() {
		return ebayToken;
	}

	public void setEbayToken(String ebayToken) {
		this.ebayToken = ebayToken;
	}

	public String getEbayDevid() {
		return ebayDevid;
	}

	public void setEbayDevid(String ebayDevid) {
		this.ebayDevid = ebayDevid;
	}

	public String getEbayAppid() {
		return ebayAppid;
	}

	public void setEbayAppid(String ebayAppid) {
		this.ebayAppid = ebayAppid;
	}

	public String getEbayCertid() {
		return ebayCertid;
	}

	public void setEbayCertid(String ebayCertid) {
		this.ebayCertid = ebayCertid;
	}

	public String getEbayRuName() {
		return ebayRuName;
	}

	public void setEbayRuName(String ebayRuName) {
		this.ebayRuName = ebayRuName;
	}

	public String getEbayApiServerUrl() {
		return ebayApiServerUrl;
	}

	public void setEbayApiServerUrl(String ebayApiServerUrl) {
		this.ebayApiServerUrl = ebayApiServerUrl;
	}

}
