package com.xuanfeiyang.erp.domain;

import java.util.Date;

/**
 * 平台账记对象简化
 * @author Administrator
 *
 */
public class PlatFormAccountSimple {
	
	private String accountName;		//平台账号
	
	private String platform;		//所属平台
	
	private String contacts;		//所属卖家
	
	private Date applyCertTime;		//资料提交时间
	
	private Integer selfFlag;		//自营
	
	private Integer vipFlag;		//Vip
	
	private Integer orderCount;		//订单数量（已审核后的订单统计）
	
	private String merchantId;      //亚马逊平台卖家ID
	
	private String marketPlaceId;    //亚马逊平台站点ID
	
	private String merchantName;     //wish平台商城名称
	
	public String getAccountName() {
		return accountName;
	}

	public void setAccountName(String accountName) {
		this.accountName = accountName;
	}

	public String getPlatform() {
		return platform;
	}

	public void setPlatform(String platform) {
		this.platform = platform;
	}

	public String getContacts() {
		return contacts;
	}

	public void setContacts(String contacts) {
		this.contacts = contacts;
	}

	public Date getApplyCertTime() {
		return applyCertTime;
	}

	public void setApplyCertTime(Date applyCertTime) {
		this.applyCertTime = applyCertTime;
	}

	public Integer getSelfFlag() {
		return selfFlag;
	}

	public void setSelfFlag(Integer selfFlag) {
		this.selfFlag = selfFlag;
	}

	public Integer getVipFlag() {
		return vipFlag;
	}

	public void setVipFlag(Integer vipFlag) {
		this.vipFlag = vipFlag;
	}

	public Integer getOrderCount() {
		return orderCount;
	}

	public void setOrderCount(Integer orderCount) {
		this.orderCount = orderCount;
	}

	public String getMerchantId() {
		return merchantId;
	}

	public void setMerchantId(String merchantId) {
		this.merchantId = merchantId;
	}

	public String getMarketPlaceId() {
		return marketPlaceId;
	}

	public void setMarketPlaceId(String marketPlaceId) {
		this.marketPlaceId = marketPlaceId;
	}

	public String getMerchantName() {
		return merchantName;
	}

	public void setMerchantName(String merchantName) {
		this.merchantName = merchantName;
	}
	
}
