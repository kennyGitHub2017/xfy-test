package com.xuanfeiyang.erp.domain;

import java.util.Date;

public class GoodsCombinationItem {
	private Integer id;

	private Integer combId;

	private String oldSku;
	
	private String newSku;

	private Integer quantity;

	private Date createdTime;

	private Date lastUpdatedTime;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getCombId() {
		return combId;
	}

	public void setCombId(Integer combId) {
		this.combId = combId;
	}

	public String getOldSku() {
		return oldSku;
	}

	public void setOldSku(String oldSku) {
		this.oldSku = oldSku;
	}

	public String getNewSku() {
		return newSku;
	}

	public void setNewSku(String newSku) {
		this.newSku = newSku;
	}

	public Integer getQuantity() {
		return quantity;
	}

	public void setQuantity(Integer quantity) {
		this.quantity = quantity;
	}

	public Date getCreatedTime() {
		return createdTime;
	}

	public void setCreatedTime(Date createdTime) {
		this.createdTime = createdTime;
	}

	public Date getLastUpdatedTime() {
		return lastUpdatedTime;
	}

	public void setLastUpdatedTime(Date lastUpdatedTime) {
		this.lastUpdatedTime = lastUpdatedTime;
	}
}