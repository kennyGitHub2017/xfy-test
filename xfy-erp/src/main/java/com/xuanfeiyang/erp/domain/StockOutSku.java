package com.xuanfeiyang.erp.domain;

public class StockOutSku {
	
	private Integer orderId; // 订单id
	private String platForm; // 订单平台
	private String orderSn; // 订单平台id
	private String createdTime; // 订单同步时间
	private String sku;
	private String itemSku; // item sku
	private String itemName; // item 名称
	private Integer itemQuantity; // sku购买数量
	private Integer stockOut; // 缺货数量
	private String buyerName;	//sku采购员
	private Integer stockOutCount;	//单个sku缺货数量统计

	public StockOutSku() {
	}

	/**
	 * 
	 * @param orderId
	 * @param platForm
	 * @param orderSn
	 * @param createdTime
	 * @param sku
	 * @param itemSku
	 * @param itemName
	 * @param itemQuantity
	 * @param stockOut
	 */
	public StockOutSku(Integer orderId, String platForm, String orderSn,
			String createdTime, String sku, String itemSku, String itemName,
			Integer itemQuantity, Integer stockOut,String buyerName) {
		super();
		this.orderId = orderId;
		this.platForm = platForm;
		this.orderSn = orderSn;
		this.createdTime = createdTime;
		this.sku = sku;
		this.itemSku = itemSku;
		this.itemName = itemName;
		this.itemQuantity = itemQuantity;
		this.stockOut = stockOut;
		this.buyerName = buyerName;
	}

	public String getSku() {
		return sku;
	}

	public void setSku(String sku) {
		this.sku = sku;
	}

	public String getOrderSn() {
		return orderSn;
	}

	public void setOrderSn(String orderSn) {
		this.orderSn = orderSn;
	}

	public String getCreatedTime() {
		return createdTime;
	}

	public void setCreatedTime(String createdTime) {
		this.createdTime = createdTime;
	}

	public String getItemSku() {
		return itemSku;
	}

	public void setItemSku(String itemSku) {
		this.itemSku = itemSku;
	}

	public String getItemName() {
		return itemName;
	}

	public void setItemName(String itemName) {
		this.itemName = itemName;
	}

	public Integer getOrderId() {
		return orderId;
	}

	public void setOrderId(Integer orderId) {
		this.orderId = orderId;
	}

	public Integer getItemQuantity() {
		return itemQuantity;
	}

	public void setItemQuantity(Integer itemQuantity) {
		this.itemQuantity = itemQuantity;
	}

	public Integer getStockOut() {
		return stockOut;
	}

	public void setStockOut(Integer stockOut) {
		this.stockOut = stockOut;
	}

	public String getPlatForm() {
		return platForm;
	}

	public void setPlatForm(String platForm) {
		this.platForm = platForm;
	}

	public String getBuyerName() {
		return buyerName;
	}

	public void setBuyerName(String buyerName) {
		this.buyerName = buyerName;
	}

	public Integer getStockOutCount() {
		return stockOutCount;
	}

	public void setStockOutCount(Integer stockOutCount) {
		this.stockOutCount = stockOutCount;
	}
}
