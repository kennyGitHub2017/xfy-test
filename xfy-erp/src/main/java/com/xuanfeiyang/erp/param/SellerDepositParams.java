package com.xuanfeiyang.erp.param;

import java.math.BigDecimal;
import java.util.Date;

import com.fasterxml.jackson.annotation.JsonFormat;

public class SellerDepositParams {
	
	//平台账号
	private String accountName;
	
	// 卖家ID
	private Integer sellerId;
	
	// 消费类型
	private Integer type;
	
	private Integer sellerType;			//1:自营卖家   0:非自营卖家   -1:Vip卖家
	
	// 开始日期
	@JsonFormat(pattern="yyyy-MM-dd", timezone="GMT+8")
	private Date startDate;
	
	// 结束日期
	@JsonFormat(pattern="yyyy-MM-dd", timezone="GMT+8")
	private Date endDate;
	
	private Integer orderId;
	
	private String endDateStr;
	
	private String startDateStr;
	
	private BigDecimal amountFrom;
	
	private BigDecimal amountTo;
	
	private Integer agentFlag;
	private Integer agentUserId;
	private String contacts;
	private Integer isCompanySeller; //是否是公司卖家 0个人 1 公司
	
	
	public Integer getSellerId() {
		return sellerId;
	}

	public void setSellerId(Integer sellerId) {
		this.sellerId = sellerId;
	}

	public Integer getType() {
		return type;
	}

	public void setType(Integer type) {
		this.type = type;
	}

	public Date getStartDate() {
		return startDate;
	}

	public void setStartDate(Date startDate) {
		this.startDate = startDate;
	}

	public Date getEndDate() {
		return endDate;
	}

	public void setEndDate(Date endDate) {
		this.endDate = endDate;
	}

	public Integer getOrderId() {
		return orderId;
	}

	public void setOrderId(Integer orderId) {
		this.orderId = orderId;
	}

	public String getEndDateStr() {
		return endDateStr;
	}

	public void setEndDateStr(String endDateStr) {
		this.endDateStr = endDateStr;
	}

	public String getStartDateStr() {
		return startDateStr;
	}

	public void setStartDateStr(String startDateStr) {
		this.startDateStr = startDateStr;
	}

	public String getAccountName() {
		return accountName;
	}

	public void setAccountName(String accountName) {
		this.accountName = accountName;
	}

	public Integer getSellerType() {
		return sellerType;
	}

	public void setSellerType(Integer sellerType) {
		this.sellerType = sellerType;
	}

	public BigDecimal getAmountFrom() {
		return amountFrom;
	}

	public void setAmountFrom(BigDecimal amountFrom) {
		this.amountFrom = amountFrom;
	}

	public BigDecimal getAmountTo() {
		return amountTo;
	}

	public void setAmountTo(BigDecimal amountTo) {
		this.amountTo = amountTo;
	}

	public Integer getAgentFlag() {
		return agentFlag;
	}

	public void setAgentFlag(Integer agentFlag) {
		this.agentFlag = agentFlag;
	}

	public Integer getAgentUserId() {
		return agentUserId;
	}

	public void setAgentUserId(Integer agentUserId) {
		this.agentUserId = agentUserId;
	}

	public String getContacts() {
		return contacts;
	}

	public void setContacts(String contacts) {
		this.contacts = contacts;
	}

	public Integer getIsCompanySeller() {
		return isCompanySeller;
	}

	public void setIsCompanySeller(Integer isCompanySeller) {
		this.isCompanySeller = isCompanySeller;
	}
	
	
}
