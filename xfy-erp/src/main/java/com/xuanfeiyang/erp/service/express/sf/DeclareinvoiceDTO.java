package com.xuanfeiyang.erp.service.express.sf;


public class DeclareinvoiceDTO {

	@Column(Mapper = "name")
	private String name;

	@Column(Mapper = "hscode")
	private String hscode;

	@Column(Mapper = "ename")
	private String ename;

	@Column(Mapper = "unit")
	private String unit;

	@Column(Mapper = "count")
	private String count;

	@Column(Mapper = "amount")
	private String amount;

	@Column(Mapper = "weight")
	private String weight;
	
	@Column(Mapper = "currency")
	private String currency;
	
	@Column(Mapper = "source_area")
	private String source_area;

	public String getSource_area() {
		return source_area;
	}

	public void setSource_area(String source_area) {
		this.source_area = source_area;
	}

	public String getCurrency() {
		return currency;
	}

	public void setCurrency(String currency) {
		this.currency = currency;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getHscode() {
		return hscode;
	}

	public void setHscode(String hscode) {
		this.hscode = hscode;
	}

	public String getEname() {
		return ename;
	}

	public void setEname(String ename) {
		this.ename = ename;
	}

	public String getUnit() {
		return unit;
	}

	public void setUnit(String unit) {
		this.unit = unit;
	}

	public String getCount() {
		return count;
	}

	public void setCount(String count) {
		this.count = count;
	}

	public String getAmount() {
		return amount;
	}

	public void setAmount(String amount) {
		this.amount = amount;
	}

	public String getWeight() {
		return weight;
	}

	public void setWeight(String weight) {
		this.weight = weight;
	}

}